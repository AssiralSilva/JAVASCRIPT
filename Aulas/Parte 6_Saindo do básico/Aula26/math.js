/*

Math 
 - pow(2,2) ---------- potência =>>2² = 4
 - sqrt     ---------- (25,2) raiz √25
 - PI       ---------- 3,14
 - ceil     ---------- teto -> arredondar pra cima
 - floor    ---------- chão -> arredondar pra baixo
 - random() ---------- gera um número aleatório entre 0 e 1.

*/

const result = Math.pow(2,2) // O segundo valor é a potência
console.log(result)

const result2 = Math.sqrt(25,2) // O segundo valor é a potência da raiz
console.log(result2)

const result3 = Math.PI // Mostra o valor de PI
console.log(result3)

const result4 = Math.ceil(3.14) // Arredonda o valor pra o mais próximo a cima
console.log(result4)

const result5 = Math.floor(3.14) // Arredonda o valor pra o mais próximo a baixo
console.log(result5)

const result6 = Math.random() // Mostra um valor aleatório entre 0 e 1.
console.log(result6)



