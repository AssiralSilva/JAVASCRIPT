/*
    Operadores de Atribuição

    = --> Atribuição
    += --> Adição
    -= --> Subtração
    -= --> Divisão
    *= --> Multiplicação
    %= --> Resto
*/

// Atribuição

const result = 20

console.log(result)


// Soma

let result2 = 20

result2 += 70 // É o mesmo que ==>  result2 = result2 + 70

console.log(result2)



// Subtração

let result3 = 20

result3 -= 70 // É o mesmo que ==>  result3 = result3 - 70

console.log(result3)



// Divisão

let result4 = 20

result4 /= 70 // result4 = result4 / 70

console.log(result4)




// Multiplicação

let result5 = 20

result5 *= 70 // result5 = result5 * 70

console.log(result5)



// Resto

let result6 = 20

result6 %= 70 // result6 = result6 % 70

console.log(result6)